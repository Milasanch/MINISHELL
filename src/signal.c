#include "../include/minishell.h"

void	sigint_handler(int sig)
{
	(void)sig;

	write(1, "\n", 1);
	rl_on_new_line();
	rl_replace_line("", 0);
	rl_redisplay();
}

void	new_signal(void)
{
	signal(SIGINT, sigint_handler);
    signal(SIGQUIT, SIG_IGN);
}